package com.codepenguins.rolling.model;

public class TextObject {
	
	private int x;
	private int y;
	private String text;
	private int fontId;
	private int color;
	
	public int getX() {
		 return x;
	}
	
	public int getY() {
		return y;
	}

	public String getText() {
		return text;
	}
	
	public int getFontId() {
		return fontId;
	}
	
	public int getColor() {
		return color;
	}
}
